package com.cydeo.task;

import com.cydeo.Color;

public class Orange {
    private int weight;
    private Color color;

    public Orange(int weight, Color color) {
        this.weight = weight;
        this.color = color;
    }

    public int getWeight() {
        return weight;
    }

    public Color getColor() {
        return color;
    }
}
